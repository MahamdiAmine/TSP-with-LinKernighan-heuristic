package TSPFileparser;

/**
 * This class represents a cartesian 2D point using its x and y coordinates
 */
public class Coordinates {
    private double x, y;

    public Coordinates(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return this.x;
    }

    public double getY() {
        return this.y;
    }


}